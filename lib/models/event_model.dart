class Event {
  final String imageUrl;
  final String category;
  final String title;
  final String price;
  final String date;
  final String location;
  final String details;

  const Event({
    required this.imageUrl,
    required this.category,
    required this.title,
    required this.price,
    required this.date,
    required this.location,
    required this.details,
  });
}
